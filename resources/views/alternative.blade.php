        <section class="intro">
            <p>In 1981, six terrifying blasts reverberated through the afternoon air and into history books. The gunshots, fired by John Hinckley, Jr., a deranged young man with an obscure obsession, nearly cut short the life of a man who would later become inextricable from American conservative politics. The assassination attempt of President Ronald Reagan not only rocked America to its core, it also paved the way for "Reaganomics"—changing the course of our political history.</p>
            <p>In 2016, Hinckley was released back into society to mixed reactions from the public, who couldn't help but wonder: Is Hinckley still a danger, or has he been rehabilitated? Below, explore how Reagan and Hinckley's paths crossed briefly and violently, as well as the intriguing events leading up to and following the shooting.</p>
